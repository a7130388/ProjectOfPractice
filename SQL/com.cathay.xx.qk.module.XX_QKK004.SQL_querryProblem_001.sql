SELECT QPF010.MEMO
  FROM DBQP.DTQPA010 QPA010
       LEFT JOIN DBQP.DTQPD030 QPD030
          ON QPA010.FUNC_ID = QPD030.FUNC_ID
             AND QPA010.PROJ_ID = QPD030.PROJ_ID
       LEFT JOIN DBQP.DTQPF010 QPF010
          ON QPA010.PB_SER_NO = QPF010.FUNC_ID
             AND QPA010.PROJ_ID = QPF010.PROJ_ID
       LEFT JOIN DBQP.DTQPA011 QPA011
          ON     QPA010.FUNC_ID = QPA011.FUNC_ID
             AND QPA010.PB_SER_NO = QPA011.PB_SER_NO
             AND QPA011.PB_STS_CD = '22'
 WHERE     QPF010.MEMO LIKE '程式問題%'
       AND QPA011.UPD_DT BETWEEN ':START_DATE' AND ':END_DATE'
       AND QPA010.PRC_DVNO = ':DIV_NO'
  WITH UR